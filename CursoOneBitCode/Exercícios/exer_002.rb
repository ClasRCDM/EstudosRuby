# Soma, Subtração e divisão
# Primeiro número
print 'Primeiro número? '
num1 = gets.chomp.to_i

# Segundo número
print 'Segundo número? '
num2 = gets.chomp.to_i

# Result
puts "A soma entre #{num1} e #{num2} é #{num1+num2}."
puts "A subtração entre #{num1} e #{num2} é #{num1-num2}."
puts "A divisão entre #{num1} e #{num2} é #{num1/num2}."
